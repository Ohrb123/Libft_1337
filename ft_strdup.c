#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s;
	size_t	i;
	char	*str;

	s = (char *)s1;
	i = 0;
	str = malloc(ft_strlen(s) + 1);
	if (str == NULL)
		return (NULL);
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
